void wlink_ramcodewrite(uint8_t *buffer, int size)
{
	unsigned long len = 64;
	int i = 0;
	while (size >= 64)
	{
		pWriteData(0, 2, buffer + 64 * i, &len);
		size -= 64;
		i++;
	}
	if (size)
	{
		len = size;
		pWriteData(0, 2, buffer + 64 * i, &len);
	}
}

