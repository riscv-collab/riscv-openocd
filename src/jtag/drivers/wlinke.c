int wlink_write(const uint8_t *buffer, uint32_t offset, uint32_t count)
{
	int packsize = 4096;

	if (riscvchip == 0x09)
		packsize = 1024;
	uint8_t *buf_bin;
	buf_bin = malloc(packsize);
	memset(buf_bin, 0xff, packsize);

	int binlength = count;
	wlink_ready_write(offset, count);

	if (binlength <= packsize)
	{
		for (int i = 0; i < count; i++)
		{
			buf_bin[i] = *(buffer + i);
		}
		int ret = wlink_fastprogram(buf_bin, packsize);
		if (ret != ERROR_OK)
		{
			return ERROR_FAIL;
		}
	}
	else
	{
		while (binlength >= packsize)
		{
			for (int i = 0; i < packsize; i++)
			{
				buf_bin[i] = *(buffer + i);
			}
			int ret = wlink_fastprogram(buf_bin, packsize);
			if (ret != ERROR_OK)
			{
				return ERROR_FAIL;
			}
			binlength -= packsize;
			buffer += packsize;
		}
		if (binlength > 0)
		{
			memset(buf_bin, 0xff, packsize);
			for (int i = 0; i < binlength; i++)
			{
				buf_bin[i] = *(buffer + i);
			}
			int ret = wlink_fastprogram(buf_bin, packsize);
			if (ret != ERROR_OK)
			{
				return ERROR_FAIL;
			}
		}
	}
	wlink_endprogram();
	return ERROR_OK;
}
